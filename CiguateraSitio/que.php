   <h3 class="section_titulo">Tratamiento de la intoxicaci&oacute;n por ciguatera</h3>            <br><br>
    <div class="info">
        <p class="justificado">Los s&iacute;ntomas incluso pueden aparecer de forma recurrente tiempo despu&eacute;s de la intoxicaci&oacute;n si el paciente consume pescado, alcohol, nueces, cafe&iacute;na, carne de pollo o cerdo, o si se somete a un ejercicio f&iacute;sico excesivo, o sufre deshidrataci&oacute;n. Por ello, se aconseja a los pacientes que durante 3-6 meses eviten el consumo de estos alimentos y se mantengan bien hidratados en todo momento para prevenir la reaparici&oacute;n de los s&iacute;ntomas.<br>
        El diagn&oacute;stico se realiza en base a los s&iacute;ntomas neurol&oacute;gicos y gastrointestinales que presenta el paciente tras la ingesti&oacute;n de pescado. No existe un tratamiento espec&iacute;fico y la intervenci&oacute;n se dirige a tratar y aliviar los s&iacute;ntomas, para lo que se suele utilizar manitor, un medicamento diur&eacute;tico.</p>
